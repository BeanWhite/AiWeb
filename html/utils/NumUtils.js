 const NumUtils = {
	getCN:function(a) {
		switch (a) {
			case 0:return '零';
				break;
			case 1:return '一';
				break;
			case 2:return '二';
				break;
			case 3:return '三';
				break;
			case 4:return '四';
				break;
			case 5:return '五';
				break;
			case 6:return '六';
				break;
			case 7:return '七';
				break;
			case 8:return '八';
				break;
			case 9:return '九';
				break;
			case 10:return '十';
				break;
			case 11:return '十一';
				break;
			case 12:return '十二';
				break;
			case 13:return '十三';
				break;
			case 14:return '十四';
				break;
			case 15:return '十五';
				break;
			case 16:return '十六';
				break;
			default:return '十七';
				break;
		}
	}
}
